//
//
void LcdDrawFastHLine(int16_t x, int16_t y, int16_t w, uint16_t color) {
  LcdDrawLine(x, y, x+w-1, y, color);
}

//
//
//
void LcdFillRect(int16_t x, int16_t y, int16_t w, int16_t h, uint16_t color) {
  int16_t i;
  for (i=x; i<x+w; i++) {
    LcdDrawFastVLine(i, y, h, color);
  }
}

//
