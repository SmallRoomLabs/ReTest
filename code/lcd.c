//
// Set  pixel
//
void LcdSetPixel(int16_t x, int16_t y, uint8_t color) {
  if ((x < 0) || (x >= LCDWIDTH) || (y < 0) || (y >= LCDHEIGHT))
    return;

  if (color) 
    lcdBuf[x+ (y/8)*LCDWIDTH] |= _BV(y%8);  
  else
    lcdBuf[x+ (y/8)*LCDWIDTH] &= ~_BV(y%8); 
}


//
// Get pixel value
//
uint8_t LcdGetPixel(int8_t x, int8_t y) {
  if ((x < 0) || (x >= LCDWIDTH) || (y < 0) || (y >= LCDHEIGHT))
    return 0;

  return (lcdBuf[x+ (y/8)*LCDWIDTH] >> (y%8)) & 0x1;  
}


//
// This function expects SPI to be already setup
//
void LcdInit(uint8_t contrast) {
  	gfxFont=NULL;

	// Set LCD signalling pins as outputs.
	DDRD |= _BV(LCD_DC_PIN) | _BV(LCD_CE_PIN) |  _BV(LCD_RST_PIN);
	PORTD |= _BV(LCD_DC_PIN) | _BV(LCD_CE_PIN) |  _BV(LCD_RST_PIN);

	// Briefly activate reset pin on LCD
	_delay_ms(100);
	CLEAR_RST_PIN; 
	_delay_ms(10);
	SET_RST_PIN;	

	LcdCommand(PCD8544_FUNCTIONSET | PCD8544_EXTENDEDINSTRUCTION );
	LcdCommand(PCD8544_SETBIAS | 0x04);
	LcdCommand(PCD8544_FUNCTIONSET);
	LcdCommand(PCD8544_DISPLAYCONTROL | PCD8544_DISPLAYNORMAL);
	LcdContrast(contrast);
	LcdRefresh();
}




//
//
//
void LcdCommand(uint8_t v) {
	CLEAR_DC_PIN;
    CLEAR_SCE_PIN;
	SpiSend(v);
    SET_SCE_PIN;
}

//
//
//
void LcdData(uint8_t v) {
	SET_DC_PIN;
    CLEAR_SCE_PIN;
	SpiSend(v);
    SET_SCE_PIN;
}

//
//
//
void LcdContrast(uint8_t val) {
	LcdCommand(PCD8544_FUNCTIONSET | PCD8544_EXTENDEDINSTRUCTION );
	LcdCommand(PCD8544_SETVOP | val); 
	LcdCommand(PCD8544_FUNCTIONSET);
}



void LcdRefresh(void) {
	uint8_t col, maxcol, p;
  
	for(p = 0; p < 6; p++) {	
		LcdCommand(PCD8544_SETYADDR | p);
	    // start at the beginning of the row
    	col = 0;
    	maxcol = LCDWIDTH-1;
		LcdCommand(PCD8544_SETXADDR | col);
		SET_DC_PIN;
		CLEAR_SCE_PIN;
	    for(; col <= maxcol; col++) {
	      	SpiSend(lcdBuf[(LCDWIDTH*p)+col]);
    	}
		SET_SCE_PIN;
	}
	LcdCommand(PCD8544_SETYADDR);
}

// clear everything
void LcdClear(void) {
  memset(lcdBuf, 0, LCDWIDTH*LCDHEIGHT/8);
  cursor_y = cursor_x = 0;
}


//
//
//
void LcdDrawLine(int16_t x0, int16_t y0, int16_t x1, int16_t y1, uint16_t color) {
  int16_t steep = abs(y1 - y0) > abs(x1 - x0);
  if (steep) {
    _swap_int16_t(x0, y0);
    _swap_int16_t(x1, y1);
  }

  if (x0 > x1) {
    _swap_int16_t(x0, x1);
    _swap_int16_t(y0, y1);
  }

  int16_t dx, dy;
  dx = x1 - x0;
  dy = abs(y1 - y0);

  int16_t err = dx / 2;
  int16_t ystep;

  if (y0 < y1) {
    ystep = 1;
  } else {
    ystep = -1;
  }

  for (; x0<=x1; x0++) {
    if (steep) {
      LcdSetPixel(y0, x0, color);
    } else {
      LcdSetPixel(x0, y0, color);
    }
    err -= dy;
    if (err < 0) {
      y0 += ystep;
      err += dx;
    }
  }
}

